package puzzlesolver.gui;

/**
 * Represents the direction of a Move; either place or remove
 *
 * @author Maikel Steneker (0753708)
 * @since 12-6-2011
 */
public enum Direction {
    /**
     * Placing a piece from the bag into the box
     */
    place,
    /**
     * Removing a piece from the box back to the bag
     */
    remove
}
